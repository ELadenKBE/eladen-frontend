Cypress.Commands.add('loginWithKeycloak', () => {
  // Assuming your Keycloak login form is at /login
  cy.visit('http://localhost:5173/');

  // Perform necessary actions to log in (fill username, password, click login button, etc.)
  cy.get('#username').type('validuser');
  cy.get('#password').type('validpassword');
  cy.get('#kc-login').click();

  // Wait for authentication and redirect to complete
  cy.url().should('not.contain', 'localhost:8080');
});
